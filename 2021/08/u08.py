class Segments:

    def __init__(self):
        self.signal = []
        self.digits = []
        self.val2segcnt = {
            0: 6,
            1: 2,
            2: 5,
            3: 5,
            4: 4,
            5: 5,
            6: 6,
            7: 3,
            8: 7,
            9: 6
        }

    def process_input(self, input: list):
        for line in input:
            signal, value = line.split(' | ')
            self.signal.append(signal.split())
            self.digits.append(value.split())

    def count_digits_with_num_segments(self, num: int):
        r = [ digit for value in self.digits for digit in value if len(digit) == num ]
        return len(r)

    def count_digits(self, digits: list):
        return sum([self.count_digits_with_num_segments(self.val2segcnt[d]) for d in digits])

    def task_a(self, input: list):
        """ task A """
        self.process_input(input)
        return self.count_digits([1, 4, 7, 8])

    def task_b(self, input: list):
        """ task B """
        return None


